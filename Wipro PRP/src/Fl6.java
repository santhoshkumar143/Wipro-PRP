import java.util.*;
class Fl6{
	public static void main(String[] args)
	{
		int n;
		Scanner s=new Scanner(System.in);
		n=s.nextInt();
		int i = 1;
		while(i<=n)		{
			for(int j=1;j<=i;j++)
			
			{
			System.out.print("*"+" ");
			
			}
			System.out.println(" ");
			i++;
			
		}
	}
}